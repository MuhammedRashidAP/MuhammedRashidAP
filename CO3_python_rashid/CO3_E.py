import statistics
list1=[1,2,3,5,5]
print("Mean: ",statistics.mean(list1))
print("Median: ",statistics.median(list1))
print("Mode: ",statistics.mode(list1))
print("Harmonic Mean: ",statistics.harmonic_mean(list1))
print("Statistics Varience: ",statistics.variance(list1))
print("Statistics Median Low: ",statistics.median_low([-12, 6.6, -3.4, 7.1, -9, 22]))